namespace Cycles
{
	internal class Program
	{
		static void Main(string[] args)
		{
			for (int i = 10; i > 0; i--)
			{
				Console.WriteLine(i);
			}



			int ab = 10;
			do
			{
				Console.WriteLine(ab);
				ab--;
			}
			while (ab > 0);



			int ac = 20;
			while (ac > 0)
			{
				Console.WriteLine(ac);
				ac--;
			}

			



			Console.ReadLine();
		}
	}
}
